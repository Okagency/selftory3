$c = $c -replace ' 합의의 동력이 무엇이냐 하면, [가-힣 ,·]{5,80}\.', ''

# === Phase 2-2: "X이 무엇이냐 — Y였다" 류 ===
$c = $c -replace ' [가-힣]{1,15}이 무엇이냐 — [가-힣 ,·]{5,60}였다\.', ''
$c = $c -replace ' [가-힣]{1,15}이 무엇이냐 — [가-힣 ,·]{5,60}이었다\.', ''

# === Phase 2-3: "X라는 단어가 평소 사전에 없었" 류 ===
$c = $c -replace ' [가-힣]{1,10}이라는 단어가 평소 [가-힣 ]{0,15}사전에[가-힣 ,·]{0,40}\.', ''
$c = $c -replace ' [가-힣]{1,10}라는 단어가 평소 [가-힣 ]{0,15}사전에[가-힣 ,·]{0,40}\.', ''
$c = $c -replace ' 평소 [가-힣 ]{0,15} 사전에 한 번도 등재된 적 없[던는] [가-힣]{0,5}\.', '.'
$c = $c -replace ' 평소 안 쓰던 단어\.', '.'

# === Phase 2-4: "그 ~ 안에 ~이 다 들어 있었다" / "그 ~ 안에 ~이 들어 있었다" ===
$c = $c -replace ' 그 [가-힣 ]{0,20}안에 [가-힣 ,·]{5,40}이 들어 있었다\.', ''
$c = $c -replace ' 그 [가-힣 ]{0,20}안에 [가-힣 ,·]{5,40}가 들어 있었다\.', ''
$c = $c -replace ' 그 [가-힣 ]{0,20}안에 [가-힣 ,·]{5,40}이 다 들어 있었다\.', ''

# === Phase 2-5: "그 시선/웃음/미소가 ~의 모습이었다" 끝맺음 ===
$c = $c -replace ' 그 시선이 [가-힣 ,·]{5,40}였다\.', '.'
$c = $c -replace ' 그 시선이 [가-힣 ,·]{5,40}이었다\.', '.'
$c = $c -replace ' 그 웃음이 [가-힣 ,·]{5,40}였다\.', '.'
$c = $c -replace ' 그 웃음이 [가-힣 ,·]{5,40}이었다\.', '.'
$c = $c -replace ' 그 미소가 [가-힣 ,·]{5,40}였다\.', '.'
$c = $c -replace ' 그 미소가 [가-힣 ,·]{5,40}이었다\.', '.'

# === Phase 2-6: "12년 동안/12년차 ~ 한 번도 ~ 적 없" 단독 분석 ===
$c = $c -replace ' 12년 동안 한 번도 [가-힣 ,·]{0,40} 적 없는 사람이었다\.', '.'
$c = $c -replace ' 12년차 PM이 12년 동안 [가-힣 ,·]{0,40}\.', '.'
$c = $c -replace ' 12년차 PM이 12년 만에 [가-힣 ,·]{0,40}였다\.', '.'

# === Phase 2-7: "정확히는 알 수 없었다" / "본 사람만 알았다" ===
$c = $c -replace ' 정확히는 알 수 없었다\.', '.'
$c = $c -replace ' 본 사람만 알았다\.', '.'
$c = $c -replace ' 본 사람만 안다\.', '.'

# === Phase 2-8: "본인도 정확히 모르는 같았다" 류 ===
$c = $c -replace ' [가-힣 ]{0,20}본인도 정확히 모르는 같았다\.', ''
$c = $c -replace ' [가-힣 ]{0,20}본인도 모르는 같았다\.', ''

# === Phase 2-9: "그러나 — 그 [수식어] 안에 [감정/단어]가 한 번 [동사]" ===
$c = $c -replace ' 그러나 — 그 [가-힣 ]{0,15}안에 [가-힣 ,·]{5,40}한 번 [가-힣]{1,10}\.', '.'

# === Phase 2-10: "또 다른 0→1." 류 잔여 ===
$c = $c -replace '\. 또 다른 0→1\.', '.'
$c = $c -replace '\. 그 0번이 1번이 됐다\.', '.'

# === Phase 2-11: "30%/70% 같은 작가의 정량 분석" ===
$c = $c -replace ' 표면 \d+% 합리, 내면 \d+% 합리, \d+% [가-힣 ]{0,20}이었다\.', ''

# === Phase 2-12: 정돈 ===
$c = $c -replace '  +', ' '
$c = $c -replace '\.\s+\.', '.'
$c = $c -replace '\s+\.', '.'
$c = $c -replace '<p>\s*</p>', ''
$c = $c -replace '<p>\s*\.\s*</p>', ''
$c = $c -replace '<p>\s+', '<p>'
$c = $c -replace '\s+</p>', '</p>'

$afterLen = $c.Length
$c | Set-Content $f -Encoding UTF8 -NoNewline

Write-Host "Phase 2 변화: $beforeLen -> $afterLen (감소 $($beforeLen - $afterLen)자)"
