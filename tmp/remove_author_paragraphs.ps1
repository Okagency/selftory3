[Console]::OutputEncoding = [System.Text.Encoding]::UTF8
$f = 'D:\dev\selftory\docs\html\ERP_컨설팅_가이드_모바일.html'
$c = Get-Content $f -Raw -Encoding UTF8

$beforeLen = $c.Length

# === 단락 단위 — 작가가 인물 심리·동기·의미 풀어주는 단락 통째 삭제 ===

# 31장 베트남 — "그 13분은..." 작가 풀이 단락
$c = $c -replace ' <p>그 13분은 — 우리 둘 다 평소보다 한 단계 더 집중했기 때문이었다\. 무엇을 집중했느냐 하면[가-힣 ,·\-—\.""''\<\>\/strongem]{0,400}끝냈다\.</p>', ''

# 베트남 출장 사유 작가 풀이 패턴
$c = $c -replace '<p>합의의 동력이 무엇이냐 하면, [가-힣 ,·\-—\.""''\<\>\/strongem]{0,300}</p>', ''

# 39장 결산 회식 후 작가 풀이 (D+5 머리카락 한 올)
$c = $c -replace '<p>그날 밤 무엇이 있었고 무엇이 없었는지, 나는 정리하지 못했다\. 다만 다음 분기 회의실에서 차이분석 Top 20 보고서를 펼치는 서책임의 손가락을 보는 내 시선이 평소와 0\.3도 달랐다\.</p>', ''

# "그 한숨에 두 톤이 섞여 있었다 — 걱정 한 톤, 약간의 감탄 한 톤" 작가 풀이
$c = $c -replace ' 그 한숨에는 두 톤이 섞여 있었다 — 걱정 한 톤, 약간의 감탄 한 톤\.', ''
$c = $c -replace ' 이번 한숨에는 감탄이 한 톤 더 보태져 있었다\.', ''

# "옆에서 그 자리를 본 사람의 자세가 머물렀다" 류
$c = $c -replace ' 그 시선 안에 — [가-힣 ,·\-—]{5,80}자세가 머물렀다\.', '.'

# "마음은 타들어가는 자리인데, 겉으론 담담한 번이었다" 도배
$c = $c -replace ' <strong>마음은 타들어가는 자리인데, 겉으론 담담한 번이었다\.</strong>', ''
$c = $c -replace ' 마음은 타들어가는 자리인데, 겉으론 담담한 번이었다\.', ''

# "그 자리를 본 사람은 회의실에 한 명이었다" 류 잔여
$c = $c -replace ' 그 자리를 본 사람은 회의실에 한 명이었다\.', ''
$c = $c -replace ' 알아챈 사람은 회의실에 한 명이었다\.', ''
$c = $c -replace ' 알아챈 사람은 [가-힣 ]{0,15}한 명이었다\.', ''

# "그 자리에 한 명이었다" 류
$c = $c -replace ' 옆에서 그 자리를 본 사람은 [가-힣 ]{0,20}한 명이었다\.', ''

# "정확히는 알 수 없었다" / "정확히 측정 안 됐다" 단독 잔여
$c = $c -replace '\. 정확히 측정 안 됐다\.', '.'
$c = $c -replace '\. 정확히 분리 안 됐다\.', '.'
$c = $c -replace '\. 정확히는 알 수 없었다\.', '.'

# "두 사람만 알고 있었다" / "표면 아래의 모습은 — 두 사람만 알고 있었다" 류
$c = $c -replace ' 표면 아래의 모습은 — 두 사람만 알고 있었다\.', ''
$c = $c -replace ' 두 사람만 알고 있었다\.', ''

# "또 다른 0→1." 잔여
$c = $c -replace ' 또 다른 0→1\.', ''
$c = $c -replace ' 그 0번이 1번이 됐다\.', ''

# "한 번도 X하지 않았다" 작가 강조 일부
$c = $c -replace ' 한 번도 그 자리를 입 밖으로 꺼내지 않았다\.', ''

# 정돈
$c = $c -replace '  +', ' '
$c = $c -replace '\.\s+\.', '.'
$c = $c -replace '\s+\.', '.'
$c = $c -replace '<p>\s*</p>', ''
$c = $c -replace '<p>\s*\.\s*</p>', ''
$c = $c -replace '<p>\s+', '<p>'
$c = $c -replace '\s+</p>', '</p>'

$afterLen = $c.Length
$c | Set-Content $f -Encoding UTF8 -NoNewline

Write-Host "단락 단위 정리: $beforeLen -> $afterLen (감소 $($beforeLen - $afterLen)자)"
