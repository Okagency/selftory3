[Console]::OutputEncoding = [System.Text.Encoding]::UTF8
$f = 'D:\dev\selftory\docs\html\ERP_컨설팅_가이드_모바일.html'
$c = Get-Content $f -Raw -Encoding UTF8

$beforeLen = $c.Length

# === Phase 1: 작가가 결론 짓는 끝맺음 패턴 ===
# "그게 X의 Y였다/이었다" 류
$c = $c -replace ' 그게 [가-힣 ,·]{0,40}의 [가-힣]{1,15}이었다\.', ''
$c = $c -replace ' 그게 [가-힣 ,·]{0,40}의 [가-힣]{1,15}였다\.', ''
$c = $c -replace ' 그게 [가-힣 ,·]{0,40}자세였다\.', ''
$c = $c -replace ' 그게 [가-힣 ,·]{0,40}자리였다\.', ''
$c = $c -replace ' 그게 [가-힣 ,·]{0,40}모습이었다\.', ''
$c = $c -replace ' 그게 [가-힣 ,·]{0,40}모습이었다\.', ''
$c = $c -replace ' 그게 [가-힣 ,·]{0,40}동력이었다\.', ''
$c = $c -replace ' 그게 [가-힣 ,·]{0,40}진짜 동력이었다\.', ''
$c = $c -replace ' 그게 [가-힣 ,·]{0,40}한 마디였다\.', ''
$c = $c -replace ' 그게 [가-힣 ,·]{0,40}측정값이었다\.', ''
$c = $c -replace ' 그게 [가-힣 ,·]{0,40}답이었다\.', ''
$c = $c -replace ' 그게 [가-힣 ,·]{0,40}흔적이었다\.', ''

# === Phase 2: "옆에서 본 사람" 단독 작가 시점 ===
$c = $c -replace ' 옆에서 그 자리를 본 사람[은이가] [가-힣 ]{0,30}한 명이었다\.', ''
$c = $c -replace ' 알아챈 사람[은이가] [가-힣 ]{0,30}한 명이었다\.', ''
$c = $c -replace ' 알아챈 사람[은이가] [가-힣 ]{0,20}이었다\.', ''
$c = $c -replace ' 옆에서 본 사람의 모습 ', ' '
$c = $c -replace ' 옆에서 본 모습으로는 분명했다\.', ''

# === Phase 3: "평소 ~ 사전에 한 번도 등재된 적 없던" 작가 분석 ===
$c = $c -replace ' 평소 [가-힣 ]{0,20}사전에 한 번도 등재된 적 없[던는]\s*[가-힣]{0,5}\.', '.'
$c = $c -replace ' 평소 [가-힣 ]{0,20}사전에 한 번도 등재된 적 없[던는]', ' 평소 안 쓰던'
$c = $c -replace ' 평소 안 쓰던 ', ''

# === Phase 4: "그 X가 ~의 측정값이었다" 잔여 ===
$c = $c -replace ' 그 \d+\.?\d*초가 [가-힣 ,·]{0,40}측정값이었다\.', ''
$c = $c -replace ' 그 \d+\.?\d*초가 [가-힣 ,·]{0,40}이었다\.', ''
$c = $c -replace ' 그 한 마디가 [가-힣 ,·]{0,40}측정값이었다\.', ''

# === Phase 5: "그 한 마디 안에 ~이 들어 있었다" 작가 풀이 ===
$c = $c -replace ' 그 한 마디 안에 [가-힣 ,·]{5,40}이 들어 있었다\.', ''
$c = $c -replace ' 그 한 마디 안에 [가-힣 ,·]{5,40}가 들어 있었다\.', ''

# === Phase 6: 시선·미소·웃음 분석 끝맺음 ===
$c = $c -replace ' 그 짧은 미소가 [가-힣 ,·]{0,30}모습이었다\.', ''
$c = $c -replace ' 그 [가-힣]+의 미소가 [가-힣 ,·]{0,30}였다\.', ''
$c = $c -replace ' 그 [가-힣]+의 웃음이 [가-힣 ,·]{0,30}였다\.', ''

# === Phase 7: 작가 분석 — "X라는 단어가 평소 ~ 사전에 없었는데" ===
$c = $c -replace ' [가-힣]+이라는 단어가 평소 [가-힣 ]{0,15}사전에[가-힣 ,·]{0,30}\.', ''
$c = $c -replace ' [가-힣]+라는 단어가 평소 [가-힣 ]{0,15}사전에[가-힣 ,·]{0,30}\.', ''

# === Phase 8: "정확히 측정 안 됐다" / "본 사람만 알았다" 류 ===
$c = $c -replace ' 정확히 측정 안 됐다\.', '.'
$c = $c -replace ' 정확히 분리 안 됐다\.', '.'
$c = $c -replace ' 정확히는 알 수 없었다\.', '.'

# === Phase 9: "또 다른 0→1." 류 단독 측정 마커 ===
$c = $c -replace ' 또 다른 0→1\.', ''
$c = $c -replace ' 또 한 번의 0→1\.', ''
$c = $c -replace ' 그 0번이 1번이 됐다\.', ''
$c = $c -replace ' 0번이 1번이 됐다\.', ''

# === Phase 10: "~ 모습이 ~ 자리에 머물렀다 사라졌다" 도배 ===
$c = $c -replace ' [가-힣 ]{5,30}모습이 [가-힣 ]{0,20}머물렀다 사라졌다\.', '.'
$c = $c -replace ' 한 번 머물렀다 사라졌다\.', '.'
$c = $c -replace ' 잠깐 머물렀다 사라졌다\.', '.'

# === Phase 11: "그게 그날 ~ 회의실의 마지막 ~이었다" 끝맺음 ===
$c = $c -replace ' 그게 그날 [가-힣 ]{0,20}회의실의 마지막 [가-힣]{0,5}이었다\.', ''
$c = $c -replace ' 그게 그날 [가-힣 ]{0,20}자리의 마지막 [가-힣]{0,5}이었다\.', ''

# === Phase 12: 정돈 ===
$c = $c -replace '  +', ' '
$c = $c -replace '\.\s+\.', '.'
$c = $c -replace '\s+\.', '.'
$c = $c -replace '<p>\s*</p>', ''
$c = $c -replace '<p>\s*\.\s*</p>', ''
$c = $c -replace '<p>\s+', '<p>'
$c = $c -replace '\s+</p>', '</p>'

$afterLen = $c.Length
$c | Set-Content $f -Encoding UTF8 -NoNewline

Write-Host "총 변화: $beforeLen -> $afterLen (감소 $($beforeLen - $afterLen)자)"
